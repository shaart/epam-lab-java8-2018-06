    class EmployerPersonPair {

        private String employer;
        private Person person;

        EmployerPersonPair(String employer, Person person) {
            this.employer = employer;
            this.person = person;
        }

        EmployerPersonPair(EmployerPersonDuration employerPersonDuration) {
            this.employer = employerPersonDuration.getEmployer();
            this.person = employerPersonDuration.getPerson();
        }

        String getEmployer() {
            return employer;
        }

        Person getPerson() {

            return person;
        }

        @Override
        public int hashCode() {
            int hash = 17 + (employer == null ? 0 : employer.hashCode());
            hash = 37 * hash + (person == null ? 0 : person.hashCode());
            return hash;
        }
    }

    /**
     * Преобразовать список сотрудников в отображение [компания -> множество людей, когда-либо работавших в этой компании].
     *
     * Входные данные:
     * [
     *     {
     *         {Иван Мельников 30},
     *         [
     *             {2, dev, "EPAM"},
     *             {1, dev, "google"}
     *         ]
     *     },
     *     {
     *         {Александр Дементьев 28},
     *         [
     *             {2, tester, "EPAM"},
     *             {1, dev, "EPAM"},
     *             {1, dev, "google"}
     *         ]
     *     },
     *     {
     *         {Дмитрий Осинов 40},
     *         [
     *             {3, QA, "yandex"},
     *             {1, QA, "EPAM"},
     *             {1, dev, "mail.ru"}
     *         ]
     *     },
     *     {
     *         {Анна Светличная 21},
     *         [
     *             {1, tester, "T-Systems"}
     *         ]
     *     }
     * ]
     *
     * Выходные данные:
     * [
     *    "EPAM" -> [
     *       {Иван Мельников 30},
     *       {Александр Дементьев 28},
     *       {Дмитрий Осинов 40}
     *    ],
     *    "google" -> [
     *       {Иван Мельников 30},
     *       {Александр Дементьев 28}
     *    ],
     *    "yandex" -> [ {Дмитрий Осинов 40} ]
     *    "mail.ru" -> [ {Дмитрий Осинов 40} ]
     *    "T-Systems" -> [ {Анна Светличная 21} ]
     * ]
     */
    @Test
    public void employersStuffList() {
        List<Employee> employees = getEmployees();

        Map<String, Set<Person>> result =
                employees.stream()
                         .flatMap(employee ->
                                 employee.getJobHistory()
                                         .stream()
                                         .map(jobHistoryEntry ->
                                                 new EmployerPersonPair(
                                                         jobHistoryEntry.getEmployer(),
                                                         employee.getPerson())))
                         .collect(groupingBy(
                                 EmployerPersonPair::getEmployer,
                                 mapping(EmployerPersonPair::getPerson,
                                         toSet())));


        Map<String, Set<Person>> expected = new HashMap<>();
        expected.put("yandex", new HashSet<>(Collections.singletonList(employees.get(2).getPerson())));
        expected.put("mail.ru", new HashSet<>(Collections.singletonList(employees.get(2).getPerson())));
        expected.put("EPAM", new HashSet<>(Arrays.asList(
            employees.get(0).getPerson(),
            employees.get(1).getPerson(),
            employees.get(4).getPerson(),
            employees.get(5).getPerson()
        )));
        expected.put("google", new HashSet<>(Arrays.asList(
            employees.get(0).getPerson(),
            employees.get(1).getPerson()
        )));
        expected.put("T-Systems", new HashSet<>(Arrays.asList(
            employees.get(3).getPerson(),
            employees.get(5).getPerson()
        )));
        assertEquals(expected, result);
    }

